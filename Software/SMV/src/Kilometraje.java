public class Kilometraje {
    
    private int Km_ini;
    private int Km_recorr;
    
    public Kilometraje(){
        Km_ini = 0;
        Km_recorr = 0;
    }

    public int getKm_ini() {
        return Km_ini;
    }

    public void setKm_ini(int Km_ini) {
        this.Km_ini = Km_ini;
    }

    public int getKm_recorr() {
        return Km_recorr;
    }

    public void setKm_recorr(int Km_recorr) {
        this.Km_recorr = Km_recorr;
    }
    
}
